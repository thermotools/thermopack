  !--------------------------------------------------------------------
  !>  Get quadrature points of active quadrature
  !!
  !!  \author Morten Hammer, 2018-11
  !  ---------------------------------------------------------------------
  subroutine get_quadrature_positions(quad,x,n)
    ! Input
    real, intent(out) :: x(max_n_quadrature)
    integer, intent(out) :: n
    integer, intent(in) :: quad
    ! Locals
    integer :: i
    select case(quad)
    case(GAUSS_10)
      n = 10
      do i=1,5
        x(i) = -x_K21(2*i)
        x(5+i) = x_K21(12-2*i)
      enddo
    case(GAUSS_KRONROD_21)
      n = 21
      do i=1,10
        x(i) = -x_K21(i)
        x(11+i) = x_K21(11-i)
      enddo
      x(11) = x_K21(11)
    case(GAUSS_15)
      n = 15
      do i=1,7
        x(i) = -x_K31(2*i)
        x(8+i) = x_K31(16-2*i)
      enddo
      x(8) = x_K31(16)
    case(GAUSS_KRONROD_31)
      n = 31
      do i=1,15
        x(i) = -x_K31(i)
        x(16+i) = x_K31(16-i)
      enddo
      x(16) = x_K31(16)
    case(GAUSS_20)
      n = 20
      do i=1,10
        x(i) = -x_K41(2*i)
